/**
 * Genera un hash simple para una cadena de texto (compatible con Unicode)
 */
function generateTextHash(text: string): string {
  let hash = 0;
  const str = text.substring(0, 100);
  
  for (let i = 0; i < str.length; i++) {
    const char = str.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32bit integer
  }
  
  // Convertir a string alfanumérico positivo
  const positiveHash = Math.abs(hash).toString(36);
  return positiveHash.substring(0, 32).padEnd(8, '0');
}

/**
 * Convierte las traducciones estáticas al formato del caché
 */
export function getPreloadCache() {
  const cache: any = {};
  
  Object.entries(staticTranslations).forEach(([text, translations]) => {
    // Genera una clave hash compatible con Unicode
    const key = generateTextHash(text);
    cache[key] = translations;
  });
  
  return cache;
}
